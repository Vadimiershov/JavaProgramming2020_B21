package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
// \n:
        System.out.println("Hello Everyone\nMy name is Vadym, I am from Illinois");
        System.out.println("====================================================");

        // \t: paragraph space
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");
        System.out.println("=====================================================");

        // \\: \ backslash
        System.out.println("/");
        System.out.println("\\");
        System.out.println("======================================================");

        // \' single quote
        System.out.println("My name is\'Vadym\'");
        System.out.println("=======================================================");

        // \" double quote
        System.out.println("My name is \"Vadym\"");


    }


}
